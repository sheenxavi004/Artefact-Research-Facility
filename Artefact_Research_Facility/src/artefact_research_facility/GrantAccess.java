    private void approveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Research_Artefact_Facility", "root","");
            Statement st = conn.createStatement();
            String details = "SELECT `name` FROM `user_info` WHERE `user_id` = '" +rid.getText()+ "'";
            ResultSet dts = st.executeQuery(details);
            dts.next();
            Uname = dts.getString("name");
            st.executeUpdate("UPDATE `user_info` SET `advisor_approved` = '1' WHERE `user_id` = '"+ rid.getText() +"'");
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
        JOptionPane.showMessageDialog(null,"Access Granted to " + Uname);
        dispose();
        new GrantAccess(Sup_id);
        
    }//GEN-LAST:event_approveActionPerformed

