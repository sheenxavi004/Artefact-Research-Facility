    public GrantAccess(int sid) {
        setTitle("Artefact Research Facility");
        setVisible(true);
        initComponents();
        try{
            Sup_id = sid;
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Research_Artefact_Facility", "root","");
            Statement st = conn.createStatement();
            String details = "SELECT `user_id`, `name` FROM `user_info` WHERE `superior_id` = '" +Sup_id + "' AND `advisor_approved` = '0'";
            ResultSet dts = st.executeQuery(details);
            DefaultTableModel model = (DefaultTableModel)(users.getModel());
            model.setRowCount(0);
            while(dts.next())
            {         
                int uid = dts.getInt("user_id");
                String name = dts.getString("name");
                Object row[] = {uid,name};
                model.addRow(row);
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }   
    }

